// Export menu items for use in other components
export function getMenuItemsForRole(role: string): MenuItem[] {
  return roleMenuItems[role] || roleMenuItems.employee;
}

export default function Sidebar() {
  const { user } = useAuth();
  const { isCollapsed, toggleSidebar } = useSidebar();
  const pathname = usePathname();
  const router = useRouter();
  const [isMobileOpen, setIsMobileOpen] = useState(false);

  // Close mobile menu when route changes
  useEffect(() => {
    setIsMobileOpen(false);
  }, [pathname]);

  // Get menu items for current user's role
  const getMenuItems = (): MenuItem[] => {
    if (!user) return [];
    return roleMenuItems[user.role] || roleMenuItems.employee;
  };

  const menuItems = getMenuItems();

  const isActive = (href: string) => {
    // Exact match
    if (pathname === href) {
      return true;
    }
    // Check if pathname is a sub-route of href
    if (pathname.startsWith(href + '/')) {
      // Check if there's a more specific menu item that also matches
      // (either exact match or a sub-route of that item)
      const moreSpecificMatch = menuItems.find(
        (item) => 
          item.href !== href && 
          (pathname === item.href || pathname.startsWith(item.href + '/')) &&
          item.href.startsWith(href + '/')
      );
      // Only active if no more specific match exists
      return !moreSpecificMatch;
    }
    return false;
  };

  const handleNavigation = (href: string) => {
    router.push(href);
    setIsMobileOpen(false);
  };

  return (
    <>
      {/* Mobile menu button */}
      <button
        onClick={() => setIsMobileOpen(!isMobileOpen)}
        className="lg:hidden fixed top-4 left-4 z-50 p-2 rounded-lg bg-white border border-gray-200 text-gray-600 shadow-md hover:bg-gray-50 transition-colors"
        aria-label="Toggle menu"
      >
        {isMobileOpen ? (
          <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
          </svg>
        ) : (
          <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
          </svg>
        )}
      </button>

      {/* Sidebar */}
      <aside
        className={`fixed top-0 left-0 z-40 h-screen bg-white border-r border-gray-200 transition-all duration-300 ease-in-out ${
          isMobileOpen ? 'translate-x-0' : '-translate-x-full lg:translate-x-0'
        } ${
          isCollapsed ? 'w-16 lg:w-16' : 'w-64 lg:w-64'
        }`}
      >
        <div className="h-full flex flex-col">
          {/* Logo and Toggle Button */}
          <div className="h-16 flex items-center justify-between px-4 border-b border-gray-200">
            {!isCollapsed && (
              <h2 className="text-xl font-bold text-gray-900">Karyasetu</h2>
              
            )}
            <button
              onClick={toggleSidebar}
              className="p-2 rounded-lg hover:bg-gray-100 text-gray-600 transition-colors ml-auto"
              aria-label={isCollapsed ? 'Expand sidebar' : 'Collapse sidebar'}
            >
              {isCollapsed ? (
                <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5l7 7-7 7" />
                </svg>
              ) : (
                <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 19l-7-7 7-7" />
                </svg>
              )}
            </button>
          </div>

          {/* Menu Items - Added padding to match spacing when search bar was present */}
          <nav className={`flex-1 px-4 py-6 space-y-2 overflow-y-auto ${isCollapsed ? 'px-2' : ''}`}>
            {menuItems.map((item) => (
              <button
                key={item.name}
                onClick={() => handleNavigation(item.href)}
                className={`w-full flex items-center rounded-lg text-sm font-medium transition-colors ${
                  isCollapsed ? 'px-2 py-3 justify-center' : 'px-4 py-3'
                } ${
                  isActive(item.href)
                    ? 'bg-blue-50 text-blue-700'
                    : 'text-gray-700 hover:bg-gray-100'
                }`}
                title={isCollapsed ? item.name : undefined}
              >
                <span className={isCollapsed ? '' : 'mr-3'}>{item.icon}</span>
                {!isCollapsed && <span>{item.name}</span>}
              </button>
            ))}
          </nav>
        </div>

        {/* Mobile overlay */}
        {isMobileOpen && (
          <div
            className="lg:hidden fixed inset-0 bg-black/50 z-30"
            onClick={() => setIsMobileOpen(false)}
          />
        )}
      </aside>
    </>
  );
}
